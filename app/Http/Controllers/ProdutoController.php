    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $oProdutos = Produto::all();
        $somatoria = 0.0;
        foreach($oProdutos as $oProduto) {
            $somatoria += $oProduto->valor;
        }
        return view('despesa/calculodespesa',['oProdutos' => $oProdutos, 'somatoria' => $somatoria]);
    }

